    public void addNewSupplier() throws SQLException {
        String supplierName = jSupplierName.getText();
        String supplierContact = jContactName.getText();
        String supplierEmail = jContactEmail.getText();
        String supplierPhone = jContactPhone.getText();
        String supplierAddress = jAddress.getText();
        String supplierServiceType = jServiceType.getText();
        String description = jDescription.getText();
        SupplierModel supplierModel = new SupplierModel(supplierName, supplierContact, supplierEmail,supplierPhone, supplierAddress, supplierServiceType, description);
        SupplierController supplierController = new SupplierController();
        boolean isAdded = supplierController.addSupplier(supplierModel);
        if(isAdded){
            JOptionPane.showMessageDialog(null, "Added Success!");
            dispose();
            SupplierView supplierView = new SupplierView();
            supplierView.setVisible(true);
        } else {
            JOptionPane.showMessageDialog(null, "Added Fail!");
        }
    }


