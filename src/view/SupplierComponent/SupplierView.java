    public void setTableData(List<SupplierModel> suppliers) {
        tableModel.setRowCount(0); // Xóa dữ liệu cũ
        for (SupplierModel supplier : suppliers) {
            Object[] row = {
                    supplier.getSupplierId(),
                    supplier.getSupplierName(),
                    supplier.getContactName(),
                    supplier.getContactEmail(),
                    supplier.getServiceType(),
            };
            tableModel.addRow(row);
        }
    }

    public void loadCustomerData() throws SQLException {
        List<SupplierModel> suppliers = supplierController.getListSupplier();
        setTableData(suppliers);
    }



