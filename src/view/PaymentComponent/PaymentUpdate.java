    public void updatePayment() throws SQLException {
        String paymentID = jPayID.getText().trim();
        String customerName = jCusName.getText().trim();
        String tourName = jTourName.getText().trim();
        String paymentDate = jDate.getText().trim();
        String paymentMethod = jMethod.getText().trim();
        String transactionId = JTrac.getText().trim();
        String status = jStatus.getText().trim();
        String amount = jAmount.getText().trim();
        if(paymentID.isEmpty() || customerName.isEmpty() || tourName.isEmpty() || paymentDate.isEmpty()
        || paymentMethod.isEmpty() || transactionId.isEmpty() || status.isEmpty() || amount.isEmpty()){
            JOptionPane.showMessageDialog(rootPane, "Please fill all the required fields!", "Error", JOptionPane.WARNING_MESSAGE);
        } else {
            payments = new PaymentModel(Integer.valueOf(paymentID), customerName, tourName, Date.valueOf(paymentDate), amount, status, paymentMethod, transactionId);
            PaymentController paymentController = new PaymentController();
            if(paymentController.updatePayment(payments)){
                JOptionPane.showMessageDialog(PaymentUpdate.this, "Updated Success!", "Success", JOptionPane.INFORMATION_MESSAGE);
                dispose();
            } else {
                JOptionPane.showMessageDialog(PaymentUpdate.this, "Update Failed!", "Fail", JOptionPane.ERROR_MESSAGE);
            }
        }

    }


